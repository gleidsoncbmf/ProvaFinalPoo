//Classe mãe abstrata
public abstract class EquipamentoEletronico {
    
    //atributos
    public String marca;
    public double preço;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public double getPreço() {
        return preço;
    }

    public void setPreço(double preço) {
        this.preço = preço;
    }

    

   
    
    
}
